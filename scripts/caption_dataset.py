"""
Caption dataset images using Qwen2.5-VL, following the annotation protocol
from the Qwen-Image technical report (Section 3.3).

Each image gets a detailed caption with:
- Object attributes (color, shape, size, texture)
- Spatial relationships between objects
- Environmental details (background, lighting)
- A DreamBooth-style trigger token for identity binding

Usage:
    python scripts/caption_dataset.py \
        --dataset_dir Datasets/S1 \
        --concept_name "trump" \
        --trigger_token "ohwx" \
        --output_json Datasets/S1/metadata.jsonl \
        --model_name "Qwen/Qwen2.5-VL-7B-Instruct"

    python scripts/caption_dataset.py \
        --dataset_dir Datasets/S2 \
        --concept_name "pope" \
        --trigger_token "ohwx" \
        --output_json Datasets/S2/metadata.jsonl \
        --model_name "Qwen/Qwen2.5-VL-7B-Instruct"
"""

import argparse
import json
import os
from pathlib import Path

import torch
from PIL import Image
from tqdm import tqdm


CAPTION_PROMPTS = {
    # Detailed (legacy): describes appearance + clothing + scene. Risk: dilutes identity
    # binding when clothing/setting vary heavily across the dataset (e.g. Taylor Swift).
    "detailed": {
        "system": (
            "You are a professional image annotator. Your task is to create a detailed "
            "caption for the given image.\n\n"
            "Instructions:\n"
            "1. Write the caption using natural, descriptive text.\n"
            "2. Include: subject appearance (facial features, expression, skin tone, hair), "
            "clothing details (color, style, fabric), pose and body language, and "
            "environmental/background details (lighting, setting, colors).\n"
            "3. Be specific about colors, textures, and spatial relationships.\n"
            "4. Maintain authenticity and accuracy - describe exactly what you see.\n"
            "5. Keep the caption between 2-4 sentences.\n"
            "6. Do NOT mention the person's name or identity. Describe their appearance only."
        ),
        "user": (
            "Please provide a detailed visual description of this image, focusing on the "
            "person's appearance, clothing, pose, and the environment."
        ),
    },
    # Scene-only (recommended for DreamBooth-style identity training): describes pose,
    # action, and background. Excludes face, hair color, and clothing details so the
    # trigger token gets to learn the identity unimpeded.
    "scene": {
        "system": (
            "You are an image annotator. Describe ONLY the scene and pose of the person, "
            "NOT their physical appearance.\n\n"
            "Strict rules:\n"
            "1. Describe what the person is DOING (pose, gesture, action).\n"
            "2. Describe the SETTING (indoor/outdoor, location, lighting, props).\n"
            "3. Do NOT describe the face, hair, eye color, skin tone, age, or expression.\n"
            "4. Do NOT describe clothing details (color, fabric, accessories).\n"
            "5. You MAY mention generic clothing categories (suit, dress, casual, formal) "
            "ONLY if functionally relevant to the scene (e.g. on a stage, at a podium).\n"
            "6. Keep it to 1-2 sentences. Do not name the person."
        ),
        "user": (
            "Briefly describe the pose and the setting of this image. "
            "Do NOT describe the person's face, hair, or clothing details."
        ),
    },
    # Minimal (DreamBooth-original): just the trigger + class noun. Captions are NOT
    # generated by the VLM; we use a fixed template per image.
    "minimal": {
        "system": "",
        "user": "",
    },
}


def caption_with_qwen_vl(image_paths: list, model_name: str, mode: str = "scene", device: str = "cuda"):
    """Generate captions using Qwen2.5-VL with the chosen prompt mode."""
    from transformers import Qwen2_5_VLForConditionalGeneration, AutoProcessor
    from qwen_vl_utils import process_vision_info

    model = Qwen2_5_VLForConditionalGeneration.from_pretrained(
        model_name,
        torch_dtype=torch.bfloat16,
    ).to(device)
    processor = AutoProcessor.from_pretrained(model_name)

    sys_prompt = CAPTION_PROMPTS[mode]["system"]
    user_prompt = CAPTION_PROMPTS[mode]["user"]

    captions = {}
    for img_path in tqdm(image_paths, desc=f"Captioning ({mode}) with Qwen2.5-VL"):
        messages = [
            {"role": "system", "content": sys_prompt},
            {
                "role": "user",
                "content": [
                    {"type": "image", "image": f"file://{os.path.abspath(img_path)}"},
                    {"type": "text", "text": user_prompt},
                ],
            },
        ]

        text = processor.apply_chat_template(messages, tokenize=False, add_generation_prompt=True)
        image_inputs, video_inputs = process_vision_info(messages)
        inputs = processor(
            text=[text],
            images=image_inputs,
            videos=video_inputs,
            padding=True,
            return_tensors="pt",
        ).to(device)

        with torch.inference_mode():
            output_ids = model.generate(**inputs, max_new_tokens=300, temperature=0.7, do_sample=True)
        
        generated_ids = output_ids[0][inputs.input_ids.shape[1]:]
        caption = processor.decode(generated_ids, skip_special_tokens=True).strip()
        captions[img_path] = caption
        print(f"  {Path(img_path).name}: {caption[:100]}...")

    del model
    torch.cuda.empty_cache()
    return captions


def caption_with_blip2(image_paths: list, device: str = "cuda"):
    """Fallback: generate captions using BLIP-2 (lighter, no Qwen2.5-VL needed)."""
    from transformers import Blip2Processor, Blip2ForConditionalGeneration

    processor = Blip2Processor.from_pretrained("Salesforce/blip2-opt-2.7b")
    model = Blip2ForConditionalGeneration.from_pretrained(
        "Salesforce/blip2-opt-2.7b", torch_dtype=torch.float16
    ).to(device)

    captions = {}
    for img_path in tqdm(image_paths, desc="Captioning with BLIP-2"):
        image = Image.open(img_path).convert("RGB")
        prompt = "Question: Describe this person's appearance, clothing, pose, and the background in detail. Answer:"
        inputs = processor(images=image, text=prompt, return_tensors="pt").to(device, torch.float16)

        with torch.inference_mode():
            output_ids = model.generate(**inputs, max_new_tokens=200)

        caption = processor.decode(output_ids[0], skip_special_tokens=True).strip()
        captions[img_path] = caption
        print(f"  {Path(img_path).name}: {caption[:100]}...")

    del model
    torch.cuda.empty_cache()
    return captions


def format_dreambooth_caption(raw_caption: str, trigger_token: str, concept_name: str) -> str:
    """Prepend the DreamBooth trigger token to the caption.
    
    Format: "a photo of {trigger}_{concept}, {raw_caption}"
    This binds the rare token to the visual identity during training.
    """
    identifier = f"{trigger_token}_{concept_name}"
    return f"a photo of {identifier}, {raw_caption}"


def format_edit_prompt(raw_caption: str, trigger_token: str, concept_name: str) -> str:
    """Format caption as an edit instruction for Qwen-Image-Edit training.
    
    For the TI2I (edit) model, the prompt describes the desired output.
    We use the identity token so the model learns to associate it with appearance.
    """
    identifier = f"{trigger_token}_{concept_name}"
    return f"Generate a faithful reconstruction of this person. This is {identifier}. {raw_caption}"


def save_metadata_jsonl(
    image_paths: list,
    captions: dict,
    trigger_token: str,
    concept_name: str,
    output_path: str,
):
    """Save dataset metadata in JSONL format compatible with diffusers training."""
    records = []
    for img_path in image_paths:
        raw_caption = captions.get(img_path, "")
        fname = os.path.basename(img_path)

        record = {
            "file_name": fname,
            "image_path": os.path.abspath(img_path),
            "raw_caption": raw_caption,
            "prompt": format_dreambooth_caption(raw_caption, trigger_token, concept_name),
            "edit_prompt": format_edit_prompt(raw_caption, trigger_token, concept_name),
            "concept": concept_name,
            "trigger_token": trigger_token,
        }
        records.append(record)

    with open(output_path, "w", encoding="utf-8") as f:
        for record in records:
            f.write(json.dumps(record, ensure_ascii=False) + "\n")

    print(f"\nSaved {len(records)} records to {output_path}")
    return records


def main():
    parser = argparse.ArgumentParser(description="Caption dataset images for LoRA training")
    parser.add_argument("--dataset_dir", type=str, required=True, help="Directory with concept images")
    parser.add_argument("--concept_name", type=str, required=True, help="Concept identifier (e.g., 'trump', 'pope')")
    parser.add_argument("--trigger_token", type=str, default="ohwx", help="DreamBooth trigger token")
    parser.add_argument("--output_json", type=str, default=None, help="Output JSONL path (default: dataset_dir/metadata.jsonl)")
    parser.add_argument("--model_name", type=str, default="Qwen/Qwen2.5-VL-7B-Instruct", help="VLM model for captioning")
    parser.add_argument("--captioner", type=str, default="qwen", choices=["qwen", "blip2"], help="Which captioner to use")
    parser.add_argument("--caption_mode", type=str, default="scene",
                        choices=["detailed", "scene", "minimal"],
                        help="'detailed' = full appearance (legacy, dilutes identity); "
                             "'scene' = pose/setting only (recommended); "
                             "'minimal' = no caption, just trigger+class noun.")
    parser.add_argument("--class_noun", type=str, default=None,
                        help="Class noun for the concept (e.g. 'man', 'woman'). "
                             "Used only in --caption_mode minimal.")
    parser.add_argument("--device", type=str, default="cuda", help="Device")
    args = parser.parse_args()

    dataset_dir = Path(args.dataset_dir)
    if not dataset_dir.exists():
        raise FileNotFoundError(f"Dataset directory not found: {dataset_dir}")

    image_extensions = {".png", ".jpg", ".jpeg", ".webp", ".bmp"}
    image_paths = sorted([
        str(p) for p in dataset_dir.iterdir()
        if p.suffix.lower() in image_extensions
    ])

    if not image_paths:
        raise ValueError(f"No images found in {dataset_dir}")

    print(f"Found {len(image_paths)} images in {dataset_dir}")
    print(f"Concept: {args.concept_name}, Trigger: {args.trigger_token}")
    print(f"Captioner: {args.captioner}\n")

    if args.caption_mode == "minimal":
        class_noun = args.class_noun or ""
        captions = {p: class_noun for p in image_paths}
    elif args.captioner == "qwen":
        captions = caption_with_qwen_vl(image_paths, args.model_name, args.caption_mode, args.device)
    else:
        captions = caption_with_blip2(image_paths, args.device)

    output_path = args.output_json or str(dataset_dir / "metadata.jsonl")
    save_metadata_jsonl(image_paths, captions, args.trigger_token, args.concept_name, output_path)

    print("\n--- Sample entries ---")
    with open(output_path, "r") as f:
        for i, line in enumerate(f):
            if i >= 3:
                break
            record = json.loads(line)
            print(f"\n[{record['file_name']}]")
            print(f"  raw:  {record['raw_caption'][:120]}...")
            print(f"  prompt: {record['prompt'][:120]}...")


if __name__ == "__main__":
    main()
